    public void readtable() {
        DefaultTableModel produtos = (DefaultTableModel) jtsecretaria.getModel();
        SecretariaDao cdao = new SecretariaDao();

        produtos.setNumRows(0);

        for (Secretaria sec : cdao.read()) {

            produtos.addRow(new Object[]{
                sec.getId(),
                sec.getNome(),
                sec.getAbreviacao(),
                sec.getData()

            });
        }
    }
    
